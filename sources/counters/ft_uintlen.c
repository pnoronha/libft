#include "libft.h"

int	ft_uintlen(unsigned int n)
{
	int	len;

	if (n >= 0 && n <= 9)
		return (1);
	len = 0;
	while (n != 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}
